/// Footprint reference for a `Foundation`.
///
/// `Polyline` is a closed XZ polyline given as an ordered list of
/// vertices (the closure is implicit; the algorithm wraps from the
/// last back to the first). `WallLoop` references an ordered list of
/// wall element ids whose XZ endpoints form a closed polygon — this
/// variant is wired for forward compatibility but its resolution is
/// deferred until arch-core walls expose stable XZ endpoint
/// accessors.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub enum FoundationFootprint {
    Polyline(Vec<DVec2>),
    WallLoop(Vec<ElementId>),
}

impl FoundationFootprint {
    /// True when this footprint can be resolved into a vertex list
    /// without further world queries.
    pub fn is_inline(&self) -> bool {
        matches!(self, Self::Polyline(_))
    }
}

/// Authored `Foundation` component (ADR-034).
///
/// Carries the parameters needed to plant a building on the active
/// `TerrainProvider`. The geometry is generated by the evaluation
/// system into a sibling `TriangleMesh` component on the same entity
/// when `NeedsEvaluation` is present.
#[derive(Component, Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct Foundation {
    pub footprint: FoundationFootprint,
    /// Y elevation of the interior floor slab, typically 0.0 or the
    /// first-floor finish level.
    pub floor_datum: f64,
    /// How far below the sampled terrain surface the foundation
    /// bottom extends (e.g. 0.3 m to account for footing thickness).
    pub below_grade_margin: f64,
    /// Maximum arc-length interval (in metres) between terrain
    /// elevation samples along each segment. Smaller = more samples,
    /// more accurate, denser mesh.
    pub sample_spacing: f64,
}

impl Foundation {
    /// Construct a polyline-based foundation.
    pub fn polyline(
        perimeter: Vec<DVec2>,
        floor_datum: f64,
        below_grade_margin: f64,
        sample_spacing: f64,
    ) -> Self {
        Self {
            footprint: FoundationFootprint::Polyline(perimeter),
            floor_datum,
            below_grade_margin,
            sample_spacing,
        }
    }
}

/// Per-foundation cache of the `TerrainVersion` value the foundation
/// was last evaluated against. When the live `TerrainVersion`
/// advances past this value the foundation is re-queued with
/// `NeedsEvaluation`.
#[derive(Component, Debug, Clone, Copy, Default, PartialEq, Serialize, Deserialize)]
pub struct LastEvaluatedTerrainVersion(pub u64);

/// Monotonic counter resource: incremented every time terrain data
/// changes (import, sculpt, reload). The `Foundation` evaluation
/// system reads this to decide whether to re-mesh.
#[derive(Resource, Debug, Default, Clone, Copy, PartialEq, Serialize, Deserialize)]
pub struct TerrainVersion(pub u64);

impl TerrainVersion {
    pub fn bump(&mut self) {
        self.0 = self.0.saturating_add(1);
    }
}

/// Errors produced by foundation evaluation.
#[derive(Debug, Clone, PartialEq)]
pub enum FoundationError {
    /// Footprint had fewer than 3 unique vertices.
    DegenerateFootprint,
    /// Polyline contains a zero-length segment that cannot be sub-
    /// sampled.
    ZeroLengthSegment,
    /// `sample_spacing` was non-positive.
    InvalidSampleSpacing,
    /// Wall-loop footprint resolution is not yet implemented; only
    /// inline polyline footprints are supported in this slice.
    WallLoopUnresolved,
    /// Terrain provider returned no elevation at one of the sample
    /// points (e.g. sample fell outside the terrain extent).
    TerrainOutOfRange { x: f64, z: f64 },
    /// No `TerrainProvider` is registered.
    NoTerrainProvider,
}

impl std::fmt::Display for FoundationError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::DegenerateFootprint => write!(f, "footprint must have at least 3 vertices"),
            Self::ZeroLengthSegment => write!(f, "footprint contains a zero-length segment"),
            Self::InvalidSampleSpacing => write!(f, "sample_spacing must be > 0"),
            Self::WallLoopUnresolved => write!(
                f,
                "WallLoop footprints are not yet resolved; use FoundationFootprint::Polyline"
            ),
            Self::TerrainOutOfRange { x, z } => {
                write!(f, "terrain has no elevation at sample ({x}, {z})")
            }
            Self::NoTerrainProvider => write!(f, "no TerrainProvider is registered"),
        }
    }
}

impl std::error::Error for FoundationError {}

/// One sub-sampled perimeter point: its XZ position and the terrain
/// elevation that was observed there.
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct PerimeterSample {
    pub xz: DVec2,
    pub terrain_y: f64,
}

/// Sub-sample a closed XZ polyline at intervals no greater than
/// `sample_spacing`.
///
/// The closing edge from the last vertex back to the first is included
/// implicitly. Per ADR-034 each sub-interval is no longer than
/// `sample_spacing`; the last sample on each segment lands exactly on
/// the segment's end vertex, so the wrap from segment to segment does
/// not double-up samples.
///
/// Returns the ordered list of XZ sample points around the perimeter,
/// without the closing duplicate.
pub fn subsample_perimeter(
    perimeter: &[DVec2],
    sample_spacing: f64,
) -> Result<Vec<DVec2>, FoundationError> {
    if perimeter.len() < 3 {
        return Err(FoundationError::DegenerateFootprint);
    }
    if !sample_spacing.is_finite() || sample_spacing <= 0.0 {
        return Err(FoundationError::InvalidSampleSpacing);
    }

    let mut samples: Vec<DVec2> = Vec::with_capacity(perimeter.len() * 2);
    for i in 0..perimeter.len() {
        let a = perimeter[i];
        let b = perimeter[(i + 1) % perimeter.len()];
        let segment = b - a;
        let length = segment.length();
        if length == 0.0 {
            return Err(FoundationError::ZeroLengthSegment);
        }
        // Number of sub-intervals (at least 1). We emit `n` points on
        // the *open* segment [a, b) so the wrap from b to the next
        // segment's a does not create a duplicate.
        let n = (length / sample_spacing).ceil().max(1.0) as usize;
        for k in 0..n {
            let t = k as f64 / n as f64;
            samples.push(a + segment * t);
        }
    }
    Ok(samples)
}

/// Signed area of a 2D polygon (shoelace). Positive when CCW. Used
/// to decide cap normal orientation.
fn projected_signed_area(polygon: &[bevy::math::Vec2]) -> f32 {
    let n = polygon.len();
    if n < 3 {
        return 0.0;
    }
    let mut sum = 0.0_f32;
    for i in 0..n {
        let a = polygon[i];
        let b = polygon[(i + 1) % n];
        sum += a.x * b.y - b.x * a.y;
    }
    sum * 0.5
}

/// Build a closed manifold `TriangleMesh` from a perimeter sample
/// sequence and per-sample terrain elevations.
///
/// Inputs are in metres, in the model coordinate frame (y is up).
/// Each perimeter sample produces two mesh vertices: a top vertex at
/// `floor_datum` and a bottom vertex at `terrain_y - below_grade_margin`.
/// Adjacent samples form quads on the perimeter, fan-triangulated
/// into the top and bottom caps.
///
/// The resulting mesh has `2 * N` perimeter vertices plus `2 * N - 4`
/// triangles for the wall, plus `2 * (N - 2)` triangles for the two
/// caps, where `N` is `samples.len()`.
pub fn build_foundation_mesh_from_samples(
    samples: &[PerimeterSample],
    floor_datum: f64,
    below_grade_margin: f64,
) -> Result<TriangleMesh, FoundationError> {
    if samples.len() < 3 {
        return Err(FoundationError::DegenerateFootprint);
    }
    let n = samples.len();
    let mut vertices: Vec<Vec3> = Vec::with_capacity(2 * n);

    // Top vertices: 0..n
    for s in samples {
        vertices.push(Vec3::new(s.xz.x as f32, floor_datum as f32, s.xz.y as f32));
    }
    // Bottom vertices: n..2n
    for s in samples {
        let y = (s.terrain_y - below_grade_margin) as f32;
        vertices.push(Vec3::new(s.xz.x as f32, y, s.xz.y as f32));
    }

    let mut faces: Vec<[u32; 3]> = Vec::new();

    // Perimeter quad strip, two triangles per quad.
    // The footprint is assumed to wind counter-clockwise when viewed
    // from above (y looking down). Outward face normals point away
    // from the interior. Each quad: top[i] - top[i+1] - bot[i+1] -
    // bot[i]; triangulated as (top[i], bot[i], bot[i+1]) and
    // (top[i], bot[i+1], top[i+1]) — outward-facing for CCW winding.
    for i in 0..n {
        let next = (i + 1) % n;
        let top_i = i as u32;
        let top_next = next as u32;
        let bot_i = (i + n) as u32;
        let bot_next = (next + n) as u32;
        faces.push([top_i, bot_i, bot_next]);
        faces.push([top_i, bot_next, top_next]);
    }

    // Top + bottom caps: triangulate the XZ projection of the
    // perimeter via ear-clipping. This is robust for non-convex
    // (L-, U-shaped) footprints; for convex inputs it produces
    // results equivalent to a fan.
    //
    // The XZ-plane signed area drives normal orientation:
    // - positive (CCW when looking down -y) → top normal points +y
    //   so we keep the ear-clipper's index order for the bottom cap
    //   and reverse for the top.
    // - negative → swap.
    let projected: Vec<bevy::math::Vec2> = samples
        .iter()
        .map(|s| bevy::math::Vec2::new(s.xz.x as f32, s.xz.y as f32))
        .collect();
    let cap_tris = ear_clip_triangulate(&projected);
    let area = projected_signed_area(&projected);
    if cap_tris.is_empty() {
        return Err(FoundationError::DegenerateFootprint);
    }
    let bot_offset = n as u32;
    for tri in cap_tris {
        let [a, b, c] = tri;
        if area > 0.0 {
            // CCW projection → top cap reversed for +y normal,
            // bottom cap kept for -y normal.
            faces.push([a, c, b]);
            faces.push([a + bot_offset, b + bot_offset, c + bot_offset]);
        } else {
            faces.push([a, b, c]);
            faces.push([a + bot_offset, c + bot_offset, b + bot_offset]);
        }
    }

    Ok(TriangleMesh {
        vertices,
        faces,
        normals: None,
        name: Some("foundation".to_string()),
    })
}

/// Bevy plugin: installs `TerrainVersion` and registers the
/// foundation evaluation system.
///
/// Apps that author foundations should add `FoundationPlugin` to their
/// plugin set. The plugin is opt-in because foundation generation
/// requires a `TerrainProviderRegistry` to be populated; an app
/// without terrain capability would gain nothing from running the
/// system.
pub struct FoundationPlugin;

impl Plugin for FoundationPlugin {
    fn build(&self, app: &mut App) {
        if !app.world().contains_resource::<TerrainVersion>() {
            app.init_resource::<TerrainVersion>();
        }
        app.add_systems(Update, evaluate_foundations_system);
    }
}

/// Evaluation system: walks every `Foundation` entity that either has
/// `NeedsEvaluation` set or has a stale `LastEvaluatedTerrainVersion`,
/// resolves the footprint, samples the terrain, builds the mesh, and
/// upserts a `TriangleMesh` + `NeedsMesh` on the same entity.
///
/// Foundations whose evaluation produces an error are left without a
/// `TriangleMesh` and will not render; the error is logged but does
/// not panic.
pub fn evaluate_foundations_system(
    mut commands: Commands,
    terrain_registry: Option<Res<TerrainProviderRegistry>>,
    terrain_version: Res<TerrainVersion>,
    foundations: Query<(
        Entity,
        &Foundation,
        Option<&LastEvaluatedTerrainVersion>,
        Option<&NeedsEvaluation>,
    )>,
    world: &World,
) {
    let provider = match terrain_registry.as_ref().and_then(|r| r.provider()) {
        Some(p) => p,
        None => {
            // No terrain registered → no work; foundations sit idle.
            return;
        }
    };
    for (entity, foundation, last_version, needs_eval) in foundations.iter() {
        let stale = match last_version {
            Some(LastEvaluatedTerrainVersion(v)) => *v != terrain_version.0,
            None => true,
        };
        if !stale && needs_eval.is_none() {
            continue;
        }
        let perimeter = match &foundation.footprint {
            FoundationFootprint::Polyline(verts) => verts.clone(),
            FoundationFootprint::WallLoop(_) => {
                bevy::log::warn!(
                    "Foundation {entity:?}: WallLoop footprints are not yet resolved; skipping",
                );
                continue;
            }
        };
        let samples = match subsample_perimeter(&perimeter, foundation.sample_spacing) {
            Ok(pts) => pts,
            Err(e) => {
                bevy::log::warn!("Foundation {entity:?}: subsample failed: {e}");
                continue;
            }
        };
        let mut sampled: Vec<PerimeterSample> = Vec::with_capacity(samples.len());
        let mut had_terrain_miss = false;
        for xz in samples {
            match provider.elevation_at(world, xz.x as f32, xz.y as f32) {
                Some(y) => sampled.push(PerimeterSample {
                    xz,
                    terrain_y: y as f64,
                }),
                None => {
                    bevy::log::warn!(
                        "Foundation {entity:?}: terrain has no elevation at ({}, {}); skipping",
                        xz.x,
                        xz.y,
                    );
                    had_terrain_miss = true;
                    break;
                }
            }
        }
        if had_terrain_miss {
            continue;
        }
        let mesh = match build_foundation_mesh_from_samples(
            &sampled,
            foundation.floor_datum,
            foundation.below_grade_margin,
        ) {
            Ok(m) => m,
            Err(e) => {
                bevy::log::warn!("Foundation {entity:?}: mesh build failed: {e}");
                continue;
            }
        };
        commands
            .entity(entity)
            .insert(mesh)
            .insert(NeedsMesh)
            .insert(LastEvaluatedTerrainVersion(terrain_version.0))
            .remove::<NeedsEvaluation>();
    }
}

