//! Foundation planting (ADR-034).
//!
//! A `Foundation` is an authored entity that extrudes a closed XZ
//! footprint downward to meet the terrain surface, producing a closed
//! manifold mesh suitable for rendering and boolean operations.
//!
//! Per ADR-034 the foundation is built by:
//!
//! 1. Walking the perimeter, sub-sampling each segment at no more than
//!    `sample_spacing` arc-length intervals.
//! 2. At every sample point, querying the active `TerrainProvider`
//!    for the ground elevation.
//! 3. Emitting a quad strip per perimeter segment whose top edge sits
//!    at `floor_datum` and whose bottom edge follows the sampled
//!    terrain minus `below_grade_margin`.
//! 4. Capping the top with a flat polygon at `floor_datum`.
//! 5. Capping the bottom with a polygon following the sampled terrain.
//!
//! The geometric algorithm (`build_foundation_mesh_from_samples`) is
//! kept as a pure function so it is fully unit-testable without a
//! Bevy world. The Bevy-side machinery is the `Foundation` component,
//! the `TerrainVersion` change-counter resource, and the evaluation
//! system that resolves the footprint, samples the terrain, and emits
//! the resulting `TriangleMesh` + `NeedsMesh`.
//!
//! Limitations of this first cut, called out in ADR-034 §Consequences:
//!
//! - Top and bottom caps are fan-triangulated. This is robust for
//!   convex footprints; non-convex (L-, U-shaped) footprints can
//!   produce overlapping triangles. A constrained-Delaunay or
//!   ear-clipping pass is the natural follow-up.
//! - Wall-loop footprint resolution is stubbed: the recipe exists in
//!   `FoundationFootprint::WallLoop` for forward compatibility but
//!   the evaluation system currently expects an explicit polyline
//!   footprint. A wall-loop resolver lands once arch-core wall
//!   recipes expose a stable XZ-endpoint accessor.

