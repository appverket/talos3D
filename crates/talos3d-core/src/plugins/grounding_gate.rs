/// Serialisable snapshot of a [`GroundingGateReport`] for MCP / model-api
/// consumption.
///
/// Callers that have a full Bevy [`World`] can build this by calling
/// [`run_grounding_gate`] on the entity's tagged claims and passing the result
/// to [`GroundingGateReportInfo::from_report`].
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct GroundingGateReportInfo {
    /// Whether all claims passed the gate.
    pub passed: bool,
    /// Serialised violations (if any).
    pub violations: Vec<GroundingGateFindingInfo>,
}

/// Serialisable form of a single [`GroundingGateFinding`].
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct GroundingGateFindingInfo {
    pub code: String,
    pub message: String,
}

impl GroundingGateReportInfo {
    /// Convert a [`GroundingGateReport`] into its serialisable form.
    pub fn from_report(report: &GroundingGateReport) -> Self {
        Self {
            passed: report.passed,
            violations: report
                .violations
                .iter()
                .map(|v| GroundingGateFindingInfo {
                    code: v.code().to_owned(),
                    message: v.message(),
                })
                .collect(),
        }
    }
}

/// Serialisable snapshot of a [`SpecCompletionTrace`] for inspection.
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct SpecCompletionTraceInfo {
    pub question_id: String,
    pub prompt_text: String,
    pub decision_kind: String,
    /// `"Low"`, `"Medium"`, or `"High"`.
    pub materiality: String,
    /// `"Low"`, `"Medium"`, or `"High"`.
    pub risk: String,
    pub grounding_tag: String,
    pub user_visible: bool,
    pub should_ask_user: bool,
    /// `"Chosen:<label>"` or `"Unresolved:<corpus_gap_id>"`.
    pub selected: String,
    pub evidence_refs: Vec<String>,
}

impl SpecCompletionTraceInfo {
    /// Convert a [`SpecCompletionTrace`] into its serialisable form.
    pub fn from_trace(trace: &SpecCompletionTrace) -> Self {
        let materiality = match trace.materiality {
            Materiality::Low => "Low",
            Materiality::Medium => "Medium",
            Materiality::High => "High",
        };
        let risk = match trace.risk {
            Risk::Low => "Low",
            Risk::Medium => "Medium",
            Risk::High => "High",
        };
        let selected = match &trace.selected {
            SelectedAlternative::Chosen { label } => format!("Chosen:{}", label),
            SelectedAlternative::Unresolved { corpus_gap_id } => {
                format!("Unresolved:{}", corpus_gap_id)
            }
        };
        Self {
            question_id: trace.question_id.clone(),
            prompt_text: trace.prompt_text.clone(),
            decision_kind: trace.decision_kind.clone(),
            materiality: materiality.to_owned(),
            risk: risk.to_owned(),
            grounding_tag: trace.grounding_tag.as_str().to_owned(),
            user_visible: trace.user_visible,
            should_ask_user: should_ask_user(trace),
            selected,
            evidence_refs: trace.evidence_refs.clone(),
        }
    }
}

