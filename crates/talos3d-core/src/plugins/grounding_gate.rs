// §2 — Spec-completion trace shape
// ---------------------------------------------------------------------------

/// Materiality of a spec-completion decision.
///
/// `High` signals that the decision has a significant impact on cost, safety,
/// or compliance; `Low` means the choice is cosmetic or easily reversible.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
pub enum Materiality {
    Low,
    Medium,
    High,
}

/// Risk of getting a spec-completion decision wrong.
///
/// `High` covers irreversible structural decisions or those with safety /
/// regulatory consequences. `Low` covers choices that are trivially correctable.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
pub enum Risk {
    Low,
    Medium,
    High,
}

/// One alternative considered during a spec-completion question.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
pub struct CandidateAlternative {
    /// Short human-readable label.
    pub label: String,
    /// The proposed value (stringified or JSON-encoded).
    pub value: String,
    /// Rationale for why this alternative is plausible.
    pub rationale: String,
    /// Epistemic tag for this candidate.
    pub grounding_tag: GroundingTag,
    /// Evidence references (passage refs, rule ids, etc.) supporting this candidate.
    pub evidence_refs: Vec<String>,
    /// Model confidence in `[0.0, 1.0]`.
    pub confidence: f32,
}

/// The selected outcome of a spec-completion decision.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
pub enum SelectedAlternative {
    /// A candidate was chosen (by the model, user, or policy).
    Chosen {
        /// Label of the chosen [`CandidateAlternative`].
        label: String,
    },
    /// No candidate could be grounded; a corpus gap was filed.
    ///
    /// The string value is the [`CorpusGapId`](crate::plugins::corpus_gap::CorpusGapId)
    /// inner string.  Stored as `String` here to remain schema-serialisable
    /// without requiring `JsonSchema` on `CorpusGapId`.  Callers should
    /// construct this from `corpus_gap_id.0`.
    Unresolved {
        /// Stable id of the filed corpus gap (e.g. `"gap-7"`).
        corpus_gap_id: String,
    },
}

/// Full record of a single spec-completion decision.
///
/// One `SpecCompletionTrace` is produced per model-generated default or
/// policy-driven value that touches user-visible design parameters.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
pub struct SpecCompletionTrace {
    /// Stable identifier for this question (e.g. `"stair.riser_max_mm"`).
    pub question_id: String,
    /// The natural-language prompt shown to the user when `user_visible` is `true`.
    pub prompt_text: String,
    /// Coarse decision kind — e.g. `"default_selection"`, `"user_override"`,
    /// `"policy_derived"`, `"corpus_gap"`.
    pub decision_kind: String,
    /// How important this decision is to the overall design.
    pub materiality: Materiality,
    /// Risk of making the wrong choice.
    pub risk: Risk,
    /// All candidates the model considered.
    pub alternatives: Vec<CandidateAlternative>,
    /// Which candidate was selected (or the gap that was filed).
    pub selected: SelectedAlternative,
    /// When `true`, the user should be prompted before committing this decision.
    pub user_visible: bool,
    /// Epistemic tag for the selected value.
    ///
    /// For a [`SelectedAlternative::Chosen`] whose selection was explicitly
    /// accepted by the user, set this to [`GroundingTag::UserSpecified`].
    /// For model-selected defaults, project the backing [`Grounding`] via
    /// [`project_grounding`].
    pub grounding_tag: GroundingTag,
    /// Evidence references that support the final selection.
    pub evidence_refs: Vec<String>,
}

/// Returns `true` when the spec-completion gate requires human confirmation
/// before committing the decision.
///
/// Per ADR-052 §2, confirmation is required when:
/// * materiality is `High`, **or**
/// * risk is `High`, **or**
/// * `user_visible` is `true` (policy explicitly marks it as a user choice).
///
/// `Medium`/`Low` combinations are auto-committed by the model.
pub fn should_ask_user(trace: &SpecCompletionTrace) -> bool {
    trace.materiality == Materiality::High || trace.risk == Risk::High || trace.user_visible
}

// ---------------------------------------------------------------------------
// §3 — Zero-hallucination grounding gate
// ---------------------------------------------------------------------------

/// A single violation emitted by [`run_grounding_gate`].
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
pub enum GroundingGateFinding {
    /// A claim carries no grounding tag; the path is provided for diagnosis.
    UntaggedClaim {
        /// The slash-delimited claim path (e.g. `"riser_max_mm"`).
        claim_path: String,
    },
    /// A claim carries a grounding *string* that is not one of the closed
    /// wire-format tags — e.g. empty, or free-text like `"seems reasonable"`.
    /// This is the hallucination case the gate exists to catch.
    UngroundedClaim {
        /// The slash-delimited claim path.
        claim_path: String,
        /// The raw, unrecognised grounding string that was supplied.
        raw: String,
    },
}

impl GroundingGateFinding {
    /// Short machine-readable code consistent with `ValidationFinding` codes
    /// elsewhere in the crate.
    pub fn code(&self) -> &'static str {
        match self {
            Self::UntaggedClaim { .. } => "grounding_gate.untagged_claim",
            Self::UngroundedClaim { .. } => "grounding_gate.ungrounded_claim",
        }
    }

    /// Human-readable description of the violation.
    pub fn message(&self) -> String {
        match self {
            Self::UntaggedClaim { claim_path } => {
                format!(
                    "claim '{}' has no grounding tag — it must carry SourceBacked, \
                     PolicyBacked, UserSpecified, or Unresolved(CorpusGap)",
                    claim_path
                )
            }
            Self::UngroundedClaim { claim_path, raw } => {
                format!(
                    "claim '{}' has an unrecognised grounding string '{}' — it must be \
                     one of: source-backed, policy-backed, user-specified, \
                     unresolved(CorpusGap)",
                    claim_path, raw
                )
            }
        }
    }
}

/// The outcome of a [`run_grounding_gate`] call.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
pub struct GroundingGateReport {
    /// `true` when all checked claims carry a valid grounding tag.
    pub passed: bool,
    /// All violations found.  Empty when `passed` is `true`.
    pub violations: Vec<GroundingGateFinding>,
}

/// An item presented to the grounding gate for validation.
///
/// The gate accepts any iterator of these; callers may construct them from
/// [`SpecCompletionTrace`] or by directly projecting a [`Grounding`].
#[derive(Debug, Clone, PartialEq)]
pub struct ClaimTagEntry<'a> {
    /// The claim path being validated.
    pub claim_path: &'a str,
    /// The resolved grounding tag, or `None` when the claim has not been tagged.
    pub tag: Option<GroundingTag>,
}

impl<'a> ClaimTagEntry<'a> {
    /// Construct an entry from a concrete tag (always `Some`).
    pub fn tagged(claim_path: &'a str, tag: GroundingTag) -> Self {
        Self {
            claim_path,
            tag: Some(tag),
        }
    }

    /// Construct an entry for an untagged claim.
    pub fn untagged(claim_path: &'a str) -> Self {
        Self {
            claim_path,
            tag: None,
        }
    }
}

/// Validate a set of claim-tag entries against the grounding gate rules
/// (ADR-052 §3).
///
/// **Rules:**
/// * Every claim must carry exactly one tag (i.e. `tag` must be `Some`).
/// * All four tag values — [`GroundingTag::SourceBacked`],
///   [`GroundingTag::PolicyBacked`], [`GroundingTag::UserSpecified`], and
///   [`GroundingTag::Unresolved`] — pass the gate.  An `Unresolved` tag
///   represents a *visible* corpus gap; it is not a hallucination.
/// * An absent (`None`) tag fails with [`GroundingGateFinding::UntaggedClaim`].
///
/// The `_traces` parameter is reserved for future rules that correlate tags
/// against trace evidence (e.g. checking that a `UserSpecified` tag has a
/// matching `SelectedAlternative::Chosen` in the trace set).
pub fn run_grounding_gate<'a>(
    claims: impl IntoIterator<Item = ClaimTagEntry<'a>>,
    _traces: &[SpecCompletionTrace],
) -> GroundingGateReport {
    let mut violations: Vec<GroundingGateFinding> = Vec::new();

    for entry in claims {
        if entry.tag.is_none() {
            violations.push(GroundingGateFinding::UntaggedClaim {
                claim_path: entry.claim_path.to_owned(),
            });
        }
        // All four tag values pass by definition; no further per-tag rules yet.
    }

    let passed = violations.is_empty();
    GroundingGateReport { passed, violations }
}

/// A model annotation presented to the gate: a claim path plus the raw grounding
/// *wire string* the model API recorded for it (e.g. from
/// `SemanticSourceRef.grounding` or `UnresolvedDecisionRecord.grounding`).
#[derive(Debug, Clone, PartialEq)]
pub struct WireClaimEntry<'a> {
    /// The claim path being validated.
    pub claim_path: &'a str,
    /// The raw grounding string recorded on the annotation.
    pub grounding: &'a str,
}

impl<'a> WireClaimEntry<'a> {
    /// Construct a wire-claim entry.
    pub fn new(claim_path: &'a str, grounding: &'a str) -> Self {
        Self {
            claim_path,
            grounding,
        }
    }
}

/// Run the zero-hallucination gate over model annotations whose groundings are
/// wire strings (ADR-052 §3).
///
/// Each grounding is parsed via [`GroundingTag::from_wire`]:
/// * a recognised tag passes — including `unresolved(CorpusGap)`, which is a
///   *declared* gap, not a hallucination;
/// * an empty/whitespace grounding fails as
///   [`GroundingGateFinding::UntaggedClaim`];
/// * any other unrecognised string (free-text, malformed) fails as
///   [`GroundingGateFinding::UngroundedClaim`] — the hallucination case.
///
/// This is the bridge that lets the gate validate real generated artifacts,
/// whose annotations carry grounding as strings rather than typed [`GroundingTag`]s.
pub fn run_grounding_gate_over_wire<'a>(
    claims: impl IntoIterator<Item = WireClaimEntry<'a>>,
) -> GroundingGateReport {
    let mut violations: Vec<GroundingGateFinding> = Vec::new();

    for entry in claims {
        if GroundingTag::from_wire(entry.grounding).is_none() {
            if entry.grounding.trim().is_empty() {
                violations.push(GroundingGateFinding::UntaggedClaim {
                    claim_path: entry.claim_path.to_owned(),
                });
            } else {
                violations.push(GroundingGateFinding::UngroundedClaim {
                    claim_path: entry.claim_path.to_owned(),
                    raw: entry.grounding.to_owned(),
                });
            }
        }
    }

    let passed = violations.is_empty();
    GroundingGateReport { passed, violations }
}

