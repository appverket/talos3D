//! PP-VILLA-1A — Spec Completion and Grounding Gate.
//!
//! Implements the "Generative Epistemic Loop" contract from ADR-052 §§1–3:
//!
//! * **§1** — A closed four-tag [`GroundingTag`] set that every claim carries,
//!   projected from the existing [`Grounding`] enum in `refinement`.
//! * **§2** — [`SpecCompletionTrace`] and companions: the spec-completion record
//!   that captures a single decision's question, alternatives, materiality, risk,
//!   evidence, and the final selection (resolved or a named [`CorpusGap`]).
//! * **§3** — A mechanical zero-hallucination validator ([`run_grounding_gate`])
//!   that inspects a set of tagged claims and traces and returns a
//!   [`GroundingGateReport`] with pass/fail and individual violations.
//!
//! This module is intentionally self-contained and **does not** modify any
//! existing enum or struct in `refinement.rs` or any other module.

use serde::{Deserialize, Serialize};

use crate::plugins::refinement::Grounding;

// ---------------------------------------------------------------------------
// §1 — Grounding tag (closed projection)
// ---------------------------------------------------------------------------

/// A closed four-value epistemic tag that every non-trivial claim must carry.
///
/// The tag is derived from a [`Grounding`] via [`project_grounding`] or
/// assigned directly at the trace level for user-accepted decisions.
///
/// ADR-052 §1 semantics:
/// * [`SourceBacked`](GroundingTag::SourceBacked) — the value is traced to a
///   corpus artefact: an explicit rule, a catalog row, or an imported document.
/// * [`PolicyBacked`](GroundingTag::PolicyBacked) — the value is generated by
///   a registered recipe or inherited via refinement (authored prior).
/// * [`UserSpecified`](GroundingTag::UserSpecified) — the user reviewed the
///   alternatives and accepted the decision; modeled here at the trace level
///   (see [`SpecCompletionTrace`]) rather than on the underlying [`Grounding`].
/// * [`Unresolved`](GroundingTag::Unresolved) — the claim could not be
///   grounded; it is a visible corpus gap that must be resolved or rejected.
///   An `Unresolved` claim **passes** the gate as a *declared* gap (not a
///   silent hallucination).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[cfg_attr(feature = "model-api", derive(schemars::JsonSchema))]
pub enum GroundingTag {
    /// Traced to a corpus artefact (rule, catalog row, or import).
    SourceBacked,
    /// Generated by a registered recipe or inherited via refinement.
    PolicyBacked,
    /// Reviewed and accepted by the user during spec completion.
    UserSpecified,
    /// Could not be grounded; a named corpus gap must be filed.
    Unresolved,
}

impl GroundingTag {
    /// Human-readable label consistent with the `as_str` convention used
    /// elsewhere in this crate.
    pub fn as_str(self) -> &'static str {
        match self {
            Self::SourceBacked => "SourceBacked",
            Self::PolicyBacked => "PolicyBacked",
            Self::UserSpecified => "UserSpecified",
            Self::Unresolved => "Unresolved",
        }
    }

    /// The canonical ADR-052 wire string for this tag.
    ///
    /// This is the on-the-wire/annotation form the model API's semantic
    /// annotations use (e.g. `SemanticSourceRef.grounding` and
    /// `UnresolvedDecisionRecord.grounding`) — distinct from
    /// [`as_str`](Self::as_str), which is the Rust-variant label. The closed set
    /// is exactly `source-backed | policy-backed | user-specified | unresolved(CorpusGap)`.
    pub fn wire_str(self) -> &'static str {
        match self {
            Self::SourceBacked => "source-backed",
            Self::PolicyBacked => "policy-backed",
            Self::UserSpecified => "user-specified",
            Self::Unresolved => "unresolved(CorpusGap)",
        }
    }

    /// Parse a grounding wire string back into a tag.
    ///
    /// Accepts exactly the four canonical [`wire_str`](Self::wire_str) forms
    /// (whitespace-trimmed). Any other value — empty, free-text, or a malformed
    /// tag — yields `None`, which the gate treats as ungrounded (a likely
    /// hallucination).
    pub fn from_wire(s: &str) -> Option<Self> {
        match s.trim() {
            "source-backed" => Some(Self::SourceBacked),
            "policy-backed" => Some(Self::PolicyBacked),
            "user-specified" => Some(Self::UserSpecified),
            "unresolved(CorpusGap)" => Some(Self::Unresolved),
            _ => None,
        }
    }
}

/// Project an existing [`Grounding`] value onto the closed [`GroundingTag`] set
/// per ADR-052 §1.
///
/// `LLMHeuristic` maps to [`GroundingTag::Unresolved`] because a raw LLM
/// inference is not an authored prior; it must be promoted to a corpus artefact
/// or filed as a gap before it can be treated as grounded.
pub fn project_grounding(g: &Grounding) -> GroundingTag {
    match g {
        Grounding::ExplicitRule(_) | Grounding::CatalogRow(_) | Grounding::Imported(_) => {
            GroundingTag::SourceBacked
        }
        Grounding::GeneratedByRecipe(_) | Grounding::Refined(_) => GroundingTag::PolicyBacked,
        Grounding::LLMHeuristic { .. } => GroundingTag::Unresolved,
    }
}

